function producto_x_id($idcentro){
    $catalogo = catalogo_completo(); 

    foreach ($catalogo as $r) {
        if ($r['id']== $idcentro) {
            return $r; 
        }
    }

    return null; 
}


/**
 * devuelve el catalogo de motos, de cada marca en particular
 */

 function catalogo_x_marcas(string $marca) : array{
    $resultado = []; 
    $catalogo = catalogo_completo(); 

    foreach ($catalogo as $r) {
        if($r['marcam'] == $marca){
            //agregar elementos al array (push)

        $resultado[] = $r;
        }

      
    }
    
    return $resultado; 


}   






